// Helper function to generate sample data for pagination
export function generateSamplePrograms(count = 10) {
    return Array.from({length: count}, (_, i) => {
        const baseProgram = programs[i % programs.length];
        return {
            id: i + 1,
            image: baseProgram.image,
            imageAlt: baseProgram.imageAlt,
            category: baseProgram.category,
            title: `${baseProgram.title} ${Math.floor(i/3) + 1}`,
            location: baseProgram.location,
            targetAudience: baseProgram.targetAudience,
            link: `${baseProgram.link}` // Add unique identifier
        };
    });
}